import {
  Bar,
  BarChart,
  ResponsiveContainer,
  Tooltip,
  XAxis,
  YAxis,
} from "recharts";
import type { Fuel, GenerationSource } from "../types/generationMix";
import type { ValueType } from "recharts/types/component/DefaultTooltipContent";

type GenerationMixChartProps = {
  sources: GenerationSource[];
};

const FUEL_COLORS: Record<Fuel, string> = {
  wind: "#60a5fa",
  solar: "#fbbf24",
  hydro: "#22d3ee",
  nuclear: "#a78bfa",
  biomass: "#84cc16",
  gas: "#f97316",
  coal: "#52525b",
  imports: "#94a3b8",
  other: "#71717a",
};

const FUEL_LABELS: Record<Fuel, string> = {
  wind: "Wind",
  solar: "Solar",
  hydro: "Hydro",
  nuclear: "Nuclear",
  biomass: "Biomass",
  gas: "Gas",
  coal: "Coal",
  imports: "Imports",
  other: "Other",
};

export function GenerationMixChart({
  sources,
}: GenerationMixChartProps): React.ReactElement {
  function CustomBarShape(props: any) {
    const { x, y, width, height, payload } = props;
    return (
      <rect
        x={x}
        y={y}
        width={width}
        height={height}
        rx={4}
        ry={4}
        fill={payload?.color ?? "#64748b"}
      />
    );
  }
  const data: GenerationSource[] = [...sources]
    .sort((a, b) => b.perc - a.perc)
    .map((source) => ({
      fuel: source.fuel,
      label: FUEL_LABELS[source.fuel],
      perc: source.perc,
      color: FUEL_COLORS[source.fuel],
    }));
  return (
    <div className="bg-[var(--surface)] border border-[var(--border)] rounded-2xl p-6 sm:p-8">
      <p className="text-[var(--muted)] text-sm uppercase tracking-wide">
        Generation mix
      </p>
      <p className="text-[var(--text)] text-sm mt-1 mb-6">
        Share of UK electricity generated by fuel type, right now
      </p>

      <ResponsiveContainer width="100%" height={320}>
        <BarChart
          data={data}
          layout={"vertical"}
          margin={{ top: 0, right: 0, left: 0, bottom: 0 }}
        >
          <XAxis
            type="number"
            domain={[0, 100]}
            tickFormatter={(v) => `${v}%`}
            stroke="var(--muted)"
            tick={{ fill: "var(--muted)", fontSize: 12 }}
          />
          <YAxis
            type="category"
            dataKey="label"
            stroke="var(--muted)"
            tick={{ fill: "var(--muted)", fontSize: 13 }}
            width={80}
          />
          <Tooltip
            cursor={{ fill: "rgba(255,255,255,0.04)" }}
            contentStyle={{
              background: "var(--surface)",
              border: "1px solid var(--border)",
              borderRadius: 8,
              color: "var(--text)",
            }}
            labelStyle={{ color: "var(--text)" }}
            itemStyle={{ color: "var(--text)" }}
            formatter={(value): [string, string] => [
              `${Number(value ?? 0).toFixed(1)}%`,
              "Share",
            ]}
          />
          <Bar
            dataKey="perc"
            radius={[0, 4, 4, 0]}
            shape={(props) => <CustomBarShape {...props} />}
          />
        </BarChart>
      </ResponsiveContainer>
    </div>
  );
}
